import { runner } from './runner'

test.each`
	value                     | result
	${'1 * 32'}               | ${32}
	${'2 * 32'}               | ${64}
	${'2 + 32'}               | ${34}
	${'2 * 2 * 3'}            | ${12}
	${'2 * 2 + 3'}            | ${7}
	${'2 + 2 * 3'}            | ${8}
	${'3 + 2 ^ 4'}            | ${19}
	${'2 ** **'}              | ${16}
	${'3 ! !'}                | ${720}
	${'4 ** / 2'}             | ${8}
	${'6 + 3 !'}              | ${12}
	${'20 + 1 * 10 - 5 * 3'}  | ${15}
	${'20 - 10 * 10 / 5 - 3'} | ${-3}
	${'2 ^ 4 - 3 ** / 3 * 5'} | ${1}
	${'2 + 2 * 2 ^ 2 ! **'}   | ${34}
`('$value => $result', ({ value, result }) => {
	expect(runner(value)).toEqual(result)
})
